    {
        $typingResult = resultsModel::create([
            'wpm' => $request->input('wpm'),
            'cpm' => $request->input('cpm'),
            'errors' => $request->input('errors'),
        ]);
        resultsModel::create($typingResult);

        return response()->json(['message' => 'Typing results saved successfully.'], 200);
    }

